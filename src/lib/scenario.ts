// Role-based layer: vertical-position context on top of size-based scenario.
// At solo scenario no role-block applies — "position in hierarchy" is N/A without a team.
export function getRoleBlock(role: string, scenario?: Scenario): string {
  if (scenario === 'solo') return '';
  if (role === 'Руководитель отдела') {
    return `\nРОЛЬ — Руководитель отдела (менеджер среднего звена):
Пользователь находится между своей командой снизу и вышестоящим руководством сверху.
- Его решения ограничены рамками установленными сверху — он применяет стратегию, не создаёт её.
- Упражнения про замысел и каскад должны учитывать обе стороны: как он передаёт замысел вниз И как сам получает задачи сверху.
- Уместно: как выглядит эта проблема когда ему самому ставят задачу без зачем — он одновременно жертва и источник этой проблемы.
- Не предлагать менять стратегию компании — только работу внутри своей зоны ответственности.`;
  }

  if (role === 'Менеджер проекта') {
    return `\nРОЛЬ — Менеджер проекта (координатор без прямого подчинения):
Пользователь управляет людьми из разных отделов которые ему формально не подчиняются, и отчитывается перед несколькими стейкхолдерами.
- "Команда" — это кросс-функциональные участники, чья мотивация зависит не только от него.
- Замысел нужно сначала согласовать со стейкхолдерами сверху, потом передать команде — это двойная работа.
- Упражнения про замысел и независимость: влияние без формальной власти — через ясность намерения, а не через контроль.
- Уместно: как добиться самостоятельности от людей которые приходят к нему как координатору, а не как начальнику.
- Не предлагать менять структуру отчётности или ставить задачи через голову функциональных руководителей.`;
  }

  if (role === 'Фаундер / CEO') {
    return `\nРОЛЬ — Фаундер / CEO (финальный принимающий решения):
Нет никого выше — все решения про стратегию, направление и "зачем" исходят от него.
- Упражнения не должны отсылать к согласованию с вышестоящим руководством.
- В небольших командах он одновременно стратег и исполнитель — это нормально, упражнения должны это учитывать.`;
  }

  // Фрилансер → solo сценарий уже покрывает всё
  // Другое → нет специфики роли, достаточно сценария по размеру
  return '';
}

// Compact one-liner describing user context — for routes that don't need full blocks
// (dialog-chat, feedback). Gives the model enough to stay consistent with exercises.
export function getContextSummary(role: string, size: string): string {
  const scenario = detectScenario(role, size);
  const sizeLabel: Record<Scenario, string> = {
    solo: 'работает один, без команды',
    micro: 'микрокоманда 2–5 человек, руководитель одновременно и управляет и делает руками',
    small: 'небольшая команда 6–15 человек',
    medium: 'средняя команда 16–50 человек, руководитель не видит работу напрямую',
    large: 'крупная организация 50+ человек, системные процессы передачи',
  };
  const roleNote = (() => {
    if (role === 'Руководитель отдела') return 'менеджер среднего звена — есть руководство сверху';
    if (role === 'Менеджер проекта') return 'координатор без прямого подчинения, есть стейкхолдеры сверху';
    if (role === 'Фаундер / CEO') return 'финальный принимающий решения, нет руководства сверху';
    if (role === 'Фрилансер') return 'фрилансер — работа с клиентами и подрядчиками';
    return '';
  })();
  const parts = [sizeLabel[scenario]];
  if (roleNote) parts.push(roleNote);
  return parts.join('; ');
}
