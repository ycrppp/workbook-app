// Shared scenario/role logic for generate, dialog-chat, feedback routes.
// Keep in sync with onboarding ROLES and SIZES:
//   ROLES: 'Фаундер / CEO' | 'Руководитель отдела' | 'Менеджер проекта' | 'Фрилансер' | 'Другое'
//   SIZES: 'Только я' | '2–5' | '6–15' | '16–50' | '50+'

export type Scenario = 'solo' | 'micro' | 'small' | 'medium' | 'large';

export function detectScenario(role: string, size: string): Scenario {
  // Фрилансер всегда соло вне зависимости от размера — у него обычно нет команды даже если "2-5" указано ошибочно.
  if (role === 'Фрилансер') return 'solo';
  if (size === 'Только я') return 'solo';
  if (size === '2–5') return 'micro';
  if (size === '6–15') return 'small';
  if (size === '16–50') return 'medium';
  if (size === '50+') return 'large';
  // Пустой/неизвестный размер — безопасный дефолт маленькая команда,
  // не large (слишком специфичный контекст для непонятного ввода).
  return 'micro';
}

