// Derived from onboarding data:
// role: Фаундер / CEO | Руководитель отдела | Менеджер проекта | Фрилансер | Другое
// size: Только я | 2–5 | 6–15 | 16–50 | 50+
function detectScenario(role: string, size: string): 'solo' | 'micro' | 'small' | 'medium' | 'large' {
  if (role === 'Фрилансер' || size === 'Только я') return 'solo';
  if (size === '2–5') return 'micro';
  if (size === '6–15') return 'small';
  if (size === '16–50') return 'medium';
  return 'large'; // 50+
}

