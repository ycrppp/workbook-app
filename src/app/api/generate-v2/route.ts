export async function POST(req: NextRequest) {
  const apiKey = process.env.ANTHROPIC_API_KEY;
  if (!apiKey) return NextResponse.json({ error: 'No API key' }, { status: 500 });

  const { action, moduleId, ex1Answer, ex2Answer } = await req.json();

  const spec = SPEC[moduleId as keyof typeof SPEC];
  if (!spec) return NextResponse.json({ error: `Unknown module: ${moduleId}` }, { status: 400 });

  try {
    if (action === 'ex1') {
      const raw = await callClaude(apiKey, spec.ex1.system, 'Создай упражнение.', 700);
      const parsed = parseJSON(raw);
      return NextResponse.json({ title: parsed.title || spec.ex1.title, instruction: parsed.instruction || '' });
    }

    if (action === 'ex2') {
      const userMsg = `Ответ пользователя на упражнение 1:\n${ex1Answer || ''}`;
      const raw = await callClaude(apiKey, spec.ex2.system, userMsg, 500);
      const parsed = parseJSON(raw);
      return NextResponse.json({ title: parsed.title || spec.ex2.title, instruction: parsed.instruction || '' });
    }

    if (action === 'ex3') {
      const userMsg = `Ответ на упражнение 1:\n${ex1Answer || ''}\n\nОтвет на упражнение 2:\n${ex2Answer || ''}`;
      const raw = await callClaude(apiKey, spec.ex3.system, userMsg, 400);
      const parsed = parseJSON(raw);
      return NextResponse.json({ title: parsed.title || spec.ex3.title, instruction: parsed.instruction || '' });
    }

    return NextResponse.json({ error: 'Unknown action' }, { status: 400 });
  } catch (err: any) {
    console.error('[generate-v2]', err);
    return NextResponse.json({ error: err.message }, { status: 500 });
  }
}
